        System.out.println("üé¨ Completing transition: currentPage=" + currentPageIndex + " -> targetPage=" + targetPageIndex);
        
        // „Éö„Éº„Ç∏„Ç§„É≥„Éá„ÉÉ„ÇØ„Çπ„ÇíÁõÆÊ®ô„Å´Êõ¥Êñ∞
